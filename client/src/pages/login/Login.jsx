import { useContext, useRef } from "react";
import "./login.css";
import { loginCall } from "../../apiCalls";
import { AuthContext } from "../../context/AuthContext";
import { CircularProgress } from "@material-ui/core";

export default function Login() {
  // useRef is like a “box” that can hold a mutable value in its .current property.
  const email = useRef();
  const password = useRef();
  //importing isFteching and dispatch from AuthContext
  const { isFetching, dispatch } = useContext(AuthContext);

  const handleClick = (e) => {
    // This prevents refreshing of the page each time
    e.preventDefault();
    loginCall(
      { //Calling current value email and password
        email: email.current.value, password: password.current.value },
      dispatch
    );
  };

  return (
    <div className="login">
      <div className="loginWrapper">
        <div className="loginLeft">
          <h3 className="loginLogo">Lamasocial</h3>
          <span className="loginDesc">
            Connect with friends and the world around you on Lamasocial.
          </span>
        </div>
        <div className="loginRight">
          <form className="loginBox" onSubmit={handleClick}>
            <input
              placeholder="Email"
              type="email"
              required
              className="loginInput"
              // If you pass a ref object to React with <div ref={myRef} />, 
              // React will set its .current property to the corresponding DOM node whenever that node changes.
              ref={email}
            />
            <input
              placeholder="Password"
              type="password"
              required
              minLength="6"
              className="loginInput"
              ref={password}
            />
            <button className="loginButton" type="submit" disabled={isFetching}>
              {/* If isFetching then show this icon else show Log In */}
              {isFetching ? (
                <CircularProgress color="white" size="20px" />
              ) : (
                "Log In"
              )}
            </button>
            <span className="loginForgot">Forgot Password?</span>
            <button className="loginRegisterButton">
              {/* If isFetching then show this icon else show Create a new account */}
              {isFetching ? (
                <CircularProgress color="white" size="20px" />
              ) : (
                "Create a New Account"
              )}
            </button>
          </form>
        </div>
      </div>
    </div>
  );
}
